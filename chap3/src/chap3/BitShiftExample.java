package chap3;

public class BitShiftExample {

	public static void main(String[] args) {
		
		// 비트 이동 연산자
		System.out.println("1<<3="+ (1<<3)); //8
		System.out.println("-8>>3="+ (-8>>3)); //-1
		
		//10진수 8 -> (00001000)
		//	뒤집고  -> (11110111)
		//		  -> 2의보수적용
		//		  -> (11111000) -> 10진수 -8
		//(-8>>3) 오른쪽으로 3칸 이동 -> (11111111) -> 10진수로 표현하려면?
		//(11111111) -> 1의보수 -> (1)(0000000) -> +1 -> (1)(0000001) -> -1
		
		//10진수 1 -> (00000001) 왼쪽으로 3칸이동 -> (00001000) -> 10진수 8
		
	}

}
